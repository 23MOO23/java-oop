
package system.beta;

class Merge {
    void System()
    {
         Selection v = new Selection();
do{
    v.Choice();
switch(v.choice)
    {
        case 'a':;
        v.processor();
        break;
        case 'b':;
        v.vcard();
        break;
        case 'c':;
        v.mobo();
        break;
        case 'd':;
        v.record();
        break;
        default:break;
    }
}while(v.choice!='e'); 
    }
}
